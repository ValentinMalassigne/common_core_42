#include "../includes/libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	s_len;
	size_t	i;
	char	*new_str;

	if (!s)
		return (NULL);
	i = 0;
	s_len = ft_strlen((char *)s);
	new_str = malloc(s_len + 1);
	if (!new_str)
		return (NULL);
	while (i < s_len)
	{
		new_str[i] = f(i, s[i]);
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
